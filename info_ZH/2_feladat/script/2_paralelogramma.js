function areaWithHeight(base, height) {
    var result;
    return base * height;
    return result;
}
function areaWithAngle(side1, side2, angleInDegrees) {
    var angleInRadians = angleInDegrees * Math.PI / 180;
    return side1 * side2 * Math.sin(angleInRadians);
}
